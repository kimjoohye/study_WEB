
 'use strict';
 
 let infinity = 10/0;
 
 // infinity : Infinity
 console.log(`infinity : ${infinity}`);
 
 let negativeInfinity = -10/0;
 
 // negativeInfinity : -Infinity
 console.log(`negativeInfinity : ${negativeInfinity}`);
 
 let nAn = 'not a number'/10;
 console.log(`nAn : ${nAn}`);
 
/* 
infinity : Infinity
negativeInfinity : -Infinity
nAn : NaN
*/